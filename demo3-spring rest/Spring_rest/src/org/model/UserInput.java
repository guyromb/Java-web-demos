package org.model;

public class UserInput {
	private String name;

	private String password;

	public void setName(String value) {
		this.name = value;
	}
	public String getName() {
		return this.name;
	}
	
	public void setPassword(String value) {
		this.password = value;
	}
	public String getPassword() {
		return this.password;
	}

}

